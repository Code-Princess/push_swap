#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*cast_s;

	cast_s = (char *)s;
	while (n > 0)
	{
		cast_s[n - 1] = 0;
		n--;
	}
	s = cast_s;
}
